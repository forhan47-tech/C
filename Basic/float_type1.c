#include <stdio.h>

int main() {
    float f = 3.14159f;            
    double d = 2.718281828459045;  
    long double ld = 1.6180339887L; 

    // Displaying values in default formats
    printf("Float: %f\n", f);              
    printf("Double: %lf\n", d);            
    printf("Long double: %Lf\n", ld);      

    return 0;
}
