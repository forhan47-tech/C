#include <stdio.h>

int main() {
    // \a - Alert/Bell
    printf("Warning!\a\n");

    // \b - Backspace
    printf("Hello\b\b\bHi\n");

    // \r - Carriage Return
    printf("Start\rEnd\n");

    // \v - Vertical Tab
    printf("Hello\vWorld\n");

    // \f - Form Feed
    printf("Page 1\fPage 2\n");

    return 0;
}
