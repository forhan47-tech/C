#include <stdio.h>

int main() {
    // \n - Newline
    printf("Line 1\nLine 2\n");

    // \t - Horizontal Tab
    printf("Name:\tNAIMUDDIN\tMarks:\t95\n");

    // \\ - Backslash
    printf("Path: C:\\Program Files\\Copilot\n");

    // \' and \" - Quotes
    printf("She said: \"Hello, NAIMUDDIN!\"\n");
    printf("It\'s a great day.\n");

    // \? - Question Mark
    printf("Is this working\?\?\n");

    return 0;
}
